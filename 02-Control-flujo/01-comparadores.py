# True or False
# Nos va a devolver un bolean(True or False)

print(1 > 2)  # Si 1 es mayor que 2, false
print(1 < 2)  # Si 2 es mayor que 1, true

print(1 <= 2)  # Si 1 es menor o igual que 2, true
print(1 >= 2)  # Si 1 es mayor o igual que 2, false

print(2 >= 2)  # Si 2 es mayor o igual que 2, true
print(2 <= 2)  # Si 2 es mayor o igual que 2, true

print(2 == 2)  # Si 2 es igual que 2. true
print(2 == 3)  # Si 3 es igual que 2. falso

print(1 == "2")  # Si int es igual a "2" es string, false
print(1 != "2")  # No es igual a, 2 es .. a 2, true
