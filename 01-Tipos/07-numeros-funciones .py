# math es un modulo (es como otro archivo que importamos)
# https://docs.python.org/3/library/math.html
import math

# La funcion de round funciona para devolver el valor al numero mas cercano
print(round(1.3))  # Se acerca mas al 1
print(round(1.7))  # Se acerca mas al 2
print(round(1.5))  # Se acerca mas al 2

# abs sirve para darle cambiarle a positivo
print(abs(55))
print(abs(-55))

# Usamos math
print(math.ceil(1.1))  # lleva al numero superior mas cercano
print(math.floor(1.99))  # lleva al numero inferiro mas cercano
